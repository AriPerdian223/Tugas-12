def luas_persegi(sisi):
    luas = sisi*sisi
    return luas

def persegi_panjang(panjang, lebar):
    luas2 = panjang*lebar
    return luas2

def jajar_genjang(alas, tinggi):
    luas3 = alas*tinggi
    return luas3

def luas_segitiga(alas, tinggi):
    luas4 = 1/2*alas*tinggi
    return luas4

def belah_ketupat(diagonal1, diagonal2):
    luas5 = 1/2*diagonal1*diagonal2
    return luas5

def ling_karan(jari):
    phi6 = 22/7
    luas6 = phi6*jari*jari
    return luas6

